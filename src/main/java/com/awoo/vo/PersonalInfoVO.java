package com.awoo.vo;

public class PersonalInfoVO {
	private int id;
	private String name;
	private String gender;
	private String birthday;
	private String password;
	private String email;
	private String telNumber;
	private String phoneNumber;
	private String address;
	private int empno;
	private int mail_auth;
	private int mail_key;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTelNumber() {
		return telNumber;
	}
	public void setTelNumber(String telNumber) {
		this.telNumber = telNumber;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	
	public int getMail_auth() {
		return mail_auth;
	}
	public void setMail_auth(int mail_auth) {
		this.mail_auth = mail_auth;
	}
	public int getMail_key() {
		return mail_key;
	}
	public void setMail_key(int mail_key) {
		this.mail_key = mail_key;
	}
}
